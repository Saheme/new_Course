package edu.javaCourse.lesson_6.homeWork.first;

public class HomeOverloading {

    int sum() {
        return 0;
    }

    int sum(int a, int b) {
        return a + b;
    }
    int sum(int a, int b, int c) {
        return a + b +c;
    }
    int sum(int a, int b, int c, int j) {
        return a + b +c +j;
    }
    int sum(int a) {
        return a ;
    }
}
